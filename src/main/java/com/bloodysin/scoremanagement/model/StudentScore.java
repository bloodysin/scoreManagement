package com.bloodysin.scoremanagement.model;

public class StudentScore {
	private String userName;
	private int value;
	private int courseId;
	private int userId;
	
	public String getUserName() {
		return userName;
	}
	
	public int getValue() {
		return value;
	}
	
	public int getCourseId() {
		return courseId;
	}
	
	public int getUserId() {
		return userId;
	}
}
